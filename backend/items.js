async function populateItems(connection) {
    const itemsResult = await connection.execute(`SELECT COUNT(*) as count FROM stock`);
    if (itemsResult.rows[0].COUNT === 0) {
        console.log('Populating stock table...');
        for (const item of items) {
            const expiryDate = item.expiryDate ? new Date(item.expiryDate) : null;
            await connection.execute(
                `INSERT INTO stock (item_code, category, item_name, price, discount, image, expiry_date, quantity) 
                 VALUES (:item_code, :category, :item_name, :price, :discount, :image, :expiry_date, :quantity)`,
                [item.itemCode, item.category, item.itemName, item.price, item.discount, item.image, expiryDate, item.quantity || 100],
                { autoCommit: true }
            );
        }
        console.log('Stock table populated.');
    }
}

module.exports = { populateItems };
